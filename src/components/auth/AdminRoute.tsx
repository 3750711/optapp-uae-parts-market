
import React, { useMemo } from 'react';
import { Navigate, useLocation } from 'react-router-dom';
import { useAuthWithProfile } from '@/hooks/useAuthWithProfile';
import { useUserAccess } from '@/hooks/useUserAccess';
import { Loader2, AlertTriangle, RefreshCw } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Alert, AlertDescription } from '@/components/ui/alert';
import { devLog } from '@/utils/logger';

interface AdminRouteProps {
  children: React.ReactNode;
  fallback?: React.ReactNode;
}

export const AdminRoute: React.FC<AdminRouteProps> = ({ 
  children, 
  fallback 
}) => {
  const { user, refreshProfile } = useAuthWithProfile();
  const { role, isFirstLoad, isAdmin } = useUserAccess();
  const location = useLocation();

  // –ú–µ–º–æ–∏–∑–∏—Ä—É–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è –ª–∏—à–Ω–∏—Ö —Ä–µ-—Ä–µ–Ω–¥–µ—Ä–æ–≤
  const authState = useMemo(() => ({
    hasUser: !!user,
    isAdmin,
    role,
    isFirstLoad,
    userId: user?.id,
    userEmail: user?.email
  }), [user, isAdmin, role, isFirstLoad]);

  devLog('üîç AdminRoute state:', authState);

  // Show spinner only on true first load
  if (authState.isFirstLoad) {
    return fallback || (
      <div className="flex items-center justify-center min-h-screen">
        <div className="text-center">
          <Loader2 className="h-8 w-8 animate-spin mx-auto mb-4" />
          <p className="text-gray-600">–ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤...</p>
          {authState.userEmail && (
            <p className="text-xs text-gray-500 mt-2">
              –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {authState.userEmail}
            </p>
          )}
        </div>
      </div>
    );
  }

  // –ù–µ –∞–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω - –ø–µ—Ä–µ–Ω–∞–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞ –ª–æ–≥–∏–Ω —Å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ–º —Ç–µ–∫—É—â–µ–≥–æ –ø—É—Ç–∏
  if (!authState.hasUser) {
    devLog('‚ùå User not authenticated, redirecting to login');
    const redirectPath = location.pathname !== '/login' ? `?from=${encodeURIComponent(location.pathname)}` : '';
    return <Navigate to={`/login${redirectPath}`} replace />;
  }

  // Check admin permissions using cached/JWT/sessionStorage role
  if (!authState.isAdmin) {
    devLog('‚ùå User does not have admin rights:', authState.role);
    return (
      <div className="min-h-screen flex items-center justify-center p-4 bg-gray-50">
        <div className="max-w-md w-full space-y-4">
          <Alert variant="destructive">
            <AlertTriangle className="h-4 w-4" />
            <AlertDescription>
              –£ –≤–∞—Å –Ω–µ—Ç –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ –¥–ª—è –¥–æ—Å—Ç—É–ø–∞ –∫ —ç—Ç–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü–µ.
              <br />
              <span className="text-xs text-gray-500 mt-1 block">
                –¢–∏–ø –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {authState.role || '–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ'}
                <br />
                Email: {authState.userEmail}
              </span>
            </AlertDescription>
          </Alert>
          <div className="flex gap-2">
            <Button 
              onClick={() => refreshProfile()}
              variant="outline"
              className="flex-1"
            >
              <RefreshCw className="h-4 w-4 mr-2" />
              –ü—Ä–æ–≤–µ—Ä–∏—Ç—å —Å–Ω–æ–≤–∞
            </Button>
            <Button 
              onClick={() => window.location.href = '/profile'}
              className="flex-1"
            >
              –í –ø—Ä–æ—Ñ–∏–ª—å
            </Button>
          </div>
        </div>
      </div>
    );
  }

  // isAdmin === true - –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç
  devLog('‚úÖ Admin access granted');
  return <>{children}</>;
};
